export function buildUserPrompt(params: GeneratorParams): string {
  const styleHints: Record<string, string> = {
    modern:    "современный технологичный тон, энергичный, инновационный",
    minimal:   "сдержанный элегантный тон, лаконичный, премиальный",
    bold:      "уверенный экспрессивный тон, прямой, сильный",
    corporate: "профессиональный деловой тон, надёжный, солидный",
  }

  const colorHints: Record<string, string> = {
    modern:    "фиолетовый #7C3AED, синий #2563EB или изумрудный #059669",
    minimal:   "чёрный #0A0A0B, тёмно-синий #1D4ED8 или сланцевый #475569",
    bold:      "яркий — красный #DC2626, оранжевый #EA580C или фиолетовый #7C3AED",
    corporate: "корпоративный синий #1D4ED8, тёмно-синий #1E3A5F или сине-серый #334155",
  }

  const lines = [
    `ТИП БИЗНЕСА: ${params.businessType}`,
    params.businessName ? `НАЗВАНИЕ (обязательно использовать): ${params.businessName}` : `НАЗВАНИЕ: придумай подходящее`,
    `ОПИСАНИЕ ОТ КЛИЕНТА: ${params.userDescription}`,
    `СТИЛЬ И ТОН: ${styleHints[params.style] ?? styleHints.modern}`,
    `РЕКОМЕНДОВАННЫЕ ЦВЕТА (accentColor): ${params.colorPreference ?? colorHints[params.style] ?? colorHints.modern}`,
    `ЯЗЫК КОНТЕНТА: ${params.language === "en" ? "английский" : params.language === "de" ? "немецкий" : "русский"}`,
    `СТРАНА/РЕГИОН: Беларусь, Минск — используй белорусские реалии (цены в $, телефоны +375)`,
  ]

  return lines.join("\n") + "\n\nСоздай JSON с контентом для этого бизнеса. Помни: только JSON, никаких объяснений."
}

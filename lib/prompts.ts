// ─── User prompt (динамический) ───────────────────────────────────────────────

export function buildUserPrompt(params: GeneratorParams): string {
  const styleGuides: Record<string, string> = {
    modern:
      "Современный технологичный стиль: тёмный или светлый фон, акцентный цвет (фиолетовый, синий или зелёный), чистая типографика, градиентные кнопки",
    minimal:
      "Минималистичный стиль: много белого пространства, строгая типографика, монохромная палитра с одним акцентом, без лишних элементов",
    bold:
      "Смелый экспрессивный стиль: крупные заголовки, яркие контрастные цвета, динамичные секции, сильные CTA",
    corporate:
      "Корпоративный профессиональный стиль: синие и серые тона, структурированный layout, внушающий доверие, официальный тон",
  }

  const languageGuides: Record<string, string> = {
    ru: "русском языке",
    en: "английском языке",
    de: "немецком языке",
  }

  return `Создай одностраничный сайт со следующими параметрами:

ТИП БИЗНЕСА: ${params.businessType}
НАЗВАНИЕ: ${params.businessName || "придумай подходящее и запоминающееся название"}
ОПИСАНИЕ ОТ КЛИЕНТА: ${params.userDescription}

СТИЛЬ ДИЗАЙНА: ${styleGuides[params.style] ?? styleGuides.modern}
${params.colorPreference ? `ЦВЕТОВЫЕ ПРЕДПОЧТЕНИЯ: ${params.colorPreference}` : ""}
ЯЗЫК КОНТЕНТА: весь текст на сайте на ${languageGuides[params.language] ?? "русском языке"}

ВАЖНО:
- Придумай реалистичное убедительное содержание: услуги с ценами, преимущества, один отзыв клиента
- Сделай сайт так, чтобы реальный бизнес мог его использовать прямо сейчас
- Номер телефона: +7 (999) 123-45-67, email: info@${params.businessName?.toLowerCase().replace(/\s/g, "") || "example"}.ru`
}

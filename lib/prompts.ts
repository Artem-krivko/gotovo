// ─── Нишевые подсказки ────────────────────────────────────────────────────────

function detectNicheHints(businessType: string): string {
  const t = businessType.toLowerCase()
  if (/стоматол|зуб/.test(t))
    return "Акцент: безболезненность (анестезия), гарантия на работы, срок протезирования, 3D-диагностика, имплантаты"
  if (/салон|красот|барбер|парикмах|маникюр|педикюр|брови|ресниц/.test(t))
    return "Акцент: портфолио работ до/после, онлайн-запись без очереди, сертифицированные мастера, используемые бренды"
  if (/ресторан|кафе|суши|пицц|бургер|шашлык|роллы/.test(t))
    return "Акцент: фирменные блюда с названиями, свежие ингредиенты, время доставки в минутах, уникальная атмосфера"
  if (/фитнес|спорт|тренаж|зал|йога|бокс|crossfit/.test(t))
    return "Акцент: конкретные трансформации клиентов (кг/недель), сертификаты тренеров, программы под цель, первое занятие"
  if (/клиник|медицин|врач|лечени|диагност|стоматол/.test(t))
    return "Акцент: стаж врачей в годах, оборудование (МРТ, УЗИ), скорость получения результатов, принимаемые страховки"
  if (/юрист|адвокат|право|нотар|юридич/.test(t))
    return "Акцент: % выигранных дел, специализация (гражданские/уголовные/корпоративные), онлайн-консультация, гонорар только за результат"
  if (/строительств|строитель|ремонт|отделк|монтаж|кровл|фасад/.test(t))
    return "Акцент: объекты в портфолио, гарантия на работы в годах, сроки, материалы с сертификатами качества, смета за 1 день"
  if (/\bit\b|айти|разработк|программ|сайт|приложен|softwаre/.test(t))
    return "Акцент: технологии (React/Python/etc), запуск за X дней, конкретные кейсы клиентов, поддержка после запуска"
  if (/курс|обучен|школ|образован|репетитор|тренинг/.test(t))
    return "Акцент: трудоустройство/результаты выпускников в %, методика, практикующие преподаватели, формат (онлайн/оффлайн)"
  if (/бухгалт|налог|аудит|учёт|1с/.test(t))
    return "Акцент: экономия на налогах в %, скорость сдачи отчётности, ответственность по договору, количество обслуживаемых компаний"
  if (/недвижим|аренда|риелтор|квартир/.test(t))
    return "Акцент: сделок за год, база объектов, средняя скорость продажи, юридическая чистота, без скрытых комиссий"
  if (/авто|шиномонтаж|сто|кузов|тюнинг/.test(t))
    return "Акцент: марки которые обслуживают, оборудование (стенд/сканер), скорость работ, гарантия на запчасти и работу"
  if (/свадьб|фото|видео|event|мероприят/.test(t))
    return "Акцент: число мероприятий в портфолио, оборудование (камеры/дроны), эксклюзивный стиль, пакеты с ценами"
  if (/доставка|логистик|курьер|транспорт/.test(t))
    return "Акцент: скорость доставки в часах, зона покрытия, GPS-трекинг, страховка, стоимость за км"
  return "Акцент: конкретные результаты клиентов в цифрах, уникальная технология или метод работы, реальные кейсы"
}

// ─── User prompt ──────────────────────────────────────────────────────────────

export function buildUserPrompt(params: GeneratorParams): string {
  const styleHints: Record<string, string> = {
    modern:    "современный технологичный тон, энергичный, инновационный",
    minimal:   "сдержанный элегантный тон, лаконичный, премиальный",
    bold:      "уверенный экспрессивный тон, прямой, сильный",
    corporate: "профессиональный деловой тон, надёжный, солидный",
  }

  const colorHints: Record<string, string> = {
    modern:    "фиолетовый #7C3AED, синий #2563EB или изумрудный #059669",
    minimal:   "чёрный #0A0A0B, тёмно-синий #1D4ED8 или сланцевый #475569",
    bold:      "яркий — красный #DC2626, оранжевый #EA580C или фиолетовый #7C3AED",
    corporate: "корпоративный синий #1D4ED8, тёмно-синий #1E3A5F или сине-серый #334155",
  }

  const lines = [
    `ТИП БИЗНЕСА: ${params.businessType}`,
    params.businessName
      ? `НАЗВАНИЕ (использовать обязательно): ${params.businessName}`
      : `НАЗВАНИЕ: придумай подходящее для ниши "${params.businessType}"`,
    `ОПИСАНИЕ ОТ КЛИЕНТА: ${params.userDescription}`,
    `СТИЛЬ И ТОН: ${styleHints[params.style] ?? styleHints.modern}`,
    `РЕКОМЕНДОВАННЫЕ ЦВЕТА (accentColor): ${params.colorPreference ?? colorHints[params.style] ?? colorHints.modern}`,
    `ЯЗЫК КОНТЕНТА: ${params.language === "en" ? "английский" : params.language === "de" ? "немецкий" : "русский"}`,
    `СТРАНА/РЕГИОН: Беларусь, Минск — используй белорусские реалии (цены в $, телефоны +375, местные названия)`,
    `НИШЕВЫЕ ПОДСКАЗКИ: ${detectNicheHints(params.businessType)}`,
    `ТРЕБОВАНИЕ СПЕЦИФИЧНОСТИ: Читатель должен сразу понять ЧЕМ именно эта компания лучше конкурентов. Никаких банальных обещаний — только конкретика.`,
  ]

  return lines.join("\n") + "\n\nСоздай JSON с контентом. Только JSON, никаких объяснений."
}
